const grid = document.getElementById("videoGrid");
const languageButtons = Array.from(document.querySelectorAll(".lang-button"));

let currentLang = localStorage.getItem("ian-demo-lang") || "zh";

function t(value) {
  return typeof value === "string" ? value : value[currentLang];
}

function renderStaticText() {
  document.documentElement.lang = currentLang === "zh" ? "zh-CN" : "en";
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.getAttribute("data-i18n");
    node.innerHTML = translations[currentLang][key];
  });
  document.getElementById("year").textContent = String(new Date().getFullYear());
  languageButtons.forEach((button) => {
    button.classList.toggle("active", button.dataset.lang === currentLang);
  });
}

function renderVideos() {
  grid.innerHTML = "";

  videos.forEach((item, idx) => {
    const card = document.createElement("article");
    card.className = "video-card";
    card.style.animationDelay = `${idx * 80}ms`;

    const src = resolveVideoSrc(item);
    card.innerHTML = `
      <h3>${t(item.title)}</h3>
      <p>${t(item.intro)}</p>
      <video controls preload="metadata" src="${src}"></video>
    `;

    grid.appendChild(card);
  });
}

function resolveVideoSrc(item) {
  if (item.videoUrl) {
    return item.videoUrl;
  }

  const name = item.releaseFileName || item.fileName;

  if (GITHUB_RELEASE_BASE_URL) {
    return `${GITHUB_RELEASE_BASE_URL}/${encodeURIComponent(name)}`;
  }

  return `${LOCAL_VIDEO_BASE}/${encodeURIComponent(name)}`;
}

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem("ian-demo-lang", lang);
  renderStaticText();
  renderVideos();
}

languageButtons.forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.lang));
});

setLanguage(currentLang);
